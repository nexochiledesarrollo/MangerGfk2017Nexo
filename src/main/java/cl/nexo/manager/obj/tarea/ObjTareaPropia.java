package cl.nexo.manager.obj.tarea;

public class ObjTareaPropia {
	int id_tareap;
	int id_usuario;
	String comentario_tareap;
	int estado_tareap;
	
	public int getId_tareap() {
		return id_tareap;
	}
	public void setId_tareap(int id_tareap) {
		this.id_tareap = id_tareap;
	}
	public int getId_usuario() {
		return id_usuario;
	}
	public void setId_usuario(int id_usuario) {
		this.id_usuario = id_usuario;
	}
	public String getComentario_tareap() {
		return comentario_tareap;
	}
	public void setComentario_tareap(String comentario_tareap) {
		this.comentario_tareap = comentario_tareap;
	}
	public int getEstado_tareap() {
		return estado_tareap;
	}
	public void setEstado_tareap(int estado_tareap) {
		this.estado_tareap = estado_tareap;
	}
	
	
}

package cl.nexo.manager.obj.conf;

public class ObjConfGeneral {
	int id_valor;
	int id_atributo;
	int id_categoria;
	String valor_atributo;
	String cadena_valor;
	int id_cliente;
	
	public int getId_valor() {
		return id_valor;
	}
	public void setId_valor(int id_valor) {
		this.id_valor = id_valor;
	}
	public int getId_atributo() {
		return id_atributo;
	}
	public void setId_atributo(int id_atributo) {
		this.id_atributo = id_atributo;
	}
	public int getId_categoria() {
		return id_categoria;
	}
	public void setId_categoria(int id_categoria) {
		this.id_categoria = id_categoria;
	}
	public String getValor_atributo() {
		return valor_atributo;
	}
	public void setValor_atributo(String valor_atributo) {
		this.valor_atributo = valor_atributo;
	}
	public String getCadena_valor() {
		return cadena_valor;
	}
	public void setCadena_valor(String cadena_valor) {
		this.cadena_valor = cadena_valor;
	}
	public int getId_cliente() {
		return id_cliente;
	}
	public void setId_cliente(int id_cliente) {
		this.id_cliente = id_cliente;
	}
	
	
}

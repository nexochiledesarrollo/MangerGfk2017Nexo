package cl.nexo.manager.obj.proyecto;

public class ObjEstudioDigitacion {
	int id_dig;
	int id_operacion;
	String verbatim_capture;
	String pre_proceso;
	
	
	public int getId_dig() {
		return id_dig;
	}
	public void setId_dig(int id_dig) {
		this.id_dig = id_dig;
	}
	public int getId_operacion() {
		return id_operacion;
	}
	public void setId_operacion(int id_operacion) {
		this.id_operacion = id_operacion;
	}
	public String getVerbatim_capture() {
		return verbatim_capture;
	}
	public void setVerbatim_capture(String verbatim_capture) {
		this.verbatim_capture = verbatim_capture;
	}
	public String getPre_proceso() {
		return pre_proceso;
	}
	public void setPre_proceso(String pre_proceso) {
		this.pre_proceso = pre_proceso;
	}
	
	
}

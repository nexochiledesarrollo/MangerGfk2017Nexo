package cl.nexo.manager.obj.nota;

public class ObjNota {
	int id_nota;
	String tarea_nota;
	String fecha_nota;
	int id_user;
	
	public int getId_nota() {
		return id_nota;
	}
	public void setId_nota(int id_nota) {
		this.id_nota = id_nota;
	}
	public String getTarea_nota() {
		return tarea_nota;
	}
	public void setTarea_nota(String tarea_nota) {
		this.tarea_nota = tarea_nota;
	}
	public String getFecha_nota() {
		return fecha_nota;
	}
	public void setFecha_nota(String fecha_nota) {
		this.fecha_nota = fecha_nota;
	}
	public int getId_user() {
		return id_user;
	}
	public void setId_user(int id_user) {
		this.id_user = id_user;
	}
	
	
}
